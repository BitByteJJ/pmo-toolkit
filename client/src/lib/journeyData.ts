/** Get a lesson by day number */
export function getLessonByDay(day: number): JourneyLesson | undefined {
  return JOURNEY_LESSONS.find(l => l.day === day);
}

/** Get the unit for a given day */
export function getUnitForDay(day: number): JourneyUnit | undefined {
  return JOURNEY_UNITS.find(u => day >= u.dayStart && day <= u.dayEnd);
}

/** Get all lessons for a unit */
export function getLessonsForUnit(unitId: string): JourneyLesson[] {
  return JOURNEY_LESSONS.filter(l => l.unit === unitId);
}

/** Total XP available in a lesson */
export function getLessonMaxXP(lesson: JourneyLesson): number {
  return lesson.questions.reduce((sum, q) => sum + q.xp, 0);
}

/** Total XP available in the entire journey */
export const TOTAL_JOURNEY_XP = JOURNEY_LESSONS.reduce(
  (sum, l) => sum + getLessonMaxXP(l),
  0
);

/** XP thresholds for levels */
export const XP_LEVELS = [
  { level: 1, title: 'PM Apprentice', minXP: 0 },
  { level: 2, title: 'PM Associate', minXP: 150 },
  { level: 3, title: 'PM Practitioner', minXP: 400 },
  { level: 4, title: 'PM Professional', minXP: 750 },
  { level: 5, title: 'PM Expert', minXP: 1200 },
  { level: 6, title: 'PM Master', minXP: 1800 },
];

export function getLevelForXP(xp: number) {
  let current = XP_LEVELS[0];
  for (const lvl of XP_LEVELS) {
    if (xp >= lvl.minXP) current = lvl;
  }
  return current;
}

export function getNextLevel(xp: number) {
  const currentLevel = getLevelForXP(xp);
  return XP_LEVELS.find(l => l.level === currentLevel.level + 1) ?? null;
}
